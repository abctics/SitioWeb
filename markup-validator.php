    <h2> ¿Qué es el "Markup Validator" del W3C?</h2>
<p>El <span class="numeracion">"Markup Validator" </span>o comprobador de marcado es un servicio gratis del W3C que te ayuda a chequear la validez de tus documentos webs. </p>

<p>La mayoría de los elementos webs son escritos usando lenguajes de marcados como HTML o XTML. Estos lenguajes están definidos por especificaciones técnicas, que usualmente incluye una gramática formal legible por máquina (y vocabulario). La acción de comprobar el código contra esas dos restricciones se llama validación y eso lo hacemos con la herramienta de de la W3C.  </p>
    
 
<p>La validación de documentos web es un importante paso que puede ayudarte a mejorar y asegurar la calidad de tu código, y además te ahorra tiempo y dinero. La validación, sin embargo, no es un control de calidad completo, ni es estrictamente equivalente a verificar la conformidad con la especificación, pero podría ayudarte mucho.
</p>


   
<p> Esta herramienta de validación puede procesar documentos escritos en la mayoría de los lenguajes de marcado. Los tipos de documentos admitidos incluyen HTML (a través de HTML 4.01) y XHTML (1.0 y 1.1) familia, MathML, SMIL y SVG (1.0 y 1.1). Además también puede validar documentos web escritos con un DTD SGML o XML, siempre que utilicen una declaración de tipo de documento adecuada. </p>

<p>Puedes validar tus documentos web de tres formas: de forma directa (copiando y pegando todo tu bloque de código ), introduciendo una url de una página o subiendo carpetas de archivos locales.Si quieres detectar errores de tus documentos web ve al siguiente<a class="enlace-simple" href="https://validator.w3.org/#validate_by_uri" target="_blank"><span class="numeracion">  enlace</span></a>y verifica tu codificación.</p>



